function pieChart() {
	$.ajax({
        url: '/chart-data',
        type: 'GET',
        data: {},
        success: function(response) {

            var TypeChart = (function () {
                var $chart = $('#chart-type');
                // Init chart
                function initPieChart($chart) {

                    var TypeChart = new Chart($chart, {
                        type: 'pie',
                        options: {
                            legend: {
                                display: true,
                            },
                        },
                        data: {
                            labels: ['Budget Spenders', 'High Spenders', 'Low Engagement', 'Moderate Spenders'],
                            datasets: [{
                                label: 'Segments',
                                backgroundColor: ["#fb6340", "#2dce89", "#1171ef", "#2b2728"],
                                data: response.redemptionOptions
                            }]
                        }
                    });

                    // Save to jQuery object
                    $chart.data('chart', TypeChart);
                }

                // Init chart
                if ($chart.length) {
                    initPieChart($chart);
                }
            })();
        }
    });
}